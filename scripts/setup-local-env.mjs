/**
 * Bootstrap .env.local from running Supabase local stack.
 *
 * Usage:
 *   pnpm db:start
 *   node scripts/setup-local-env.mjs
 */
import { execSync } from "node:child_process";
import { existsSync, readFileSync, writeFileSync } from "node:fs";

const ENV_FILE = ".env.local";
const EXAMPLE_FILE = ".env.local.example";

function parseEnvOutput(output) {
  const vars = {};
  for (const line of output.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq);
    let value = trimmed.slice(eq + 1);
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    vars[key] = value;
  }
  return vars;
}

function parseEnvFile(path) {
  if (!existsSync(path)) return {};
  return parseEnvOutput(readFileSync(path, "utf8"));
}

function serializeEnv(vars) {
  const order = [
    "VITE_SUPABASE_URL",
    "VITE_SUPABASE_ANON_KEY",
    "SUPABASE_SERVICE_ROLE_KEY",
    "ADMIN_EMAIL",
    "ADMIN_PASSWORD",
    "VITE_CURRENCY",
    "VITE_LOCALE",
  ];

  const lines = [
    "# Generated by scripts/setup-local-env.mjs",
    "# Local Supabase (Docker via pnpm db:start)",
    "",
  ];

  const written = new Set();
  for (const key of order) {
    if (vars[key] != null) {
      lines.push(`${key}=${vars[key]}`);
      written.add(key);
    }
  }

  for (const [key, value] of Object.entries(vars)) {
    if (!written.has(key)) lines.push(`${key}=${value}`);
  }

  lines.push("");
  return lines.join("\n");
}

let statusVars;
try {
  const output = execSync("pnpm exec supabase status -o env", {
    encoding: "utf8",
    stdio: ["pipe", "pipe", "pipe"],
  });
  statusVars = parseEnvOutput(output);
} catch (err) {
  console.error(
    "Could not read Supabase status. Is the local stack running?\n  pnpm db:start",
  );
  if (err.stderr) console.error(err.stderr.toString());
  process.exit(1);
}

const existing = parseEnvFile(ENV_FILE);
const example = parseEnvFile(EXAMPLE_FILE);

const merged = {
  ...example,
  ...existing,
  VITE_SUPABASE_URL: statusVars.API_URL ?? "http://127.0.0.1:54321",
  VITE_SUPABASE_ANON_KEY: statusVars.ANON_KEY ?? existing.VITE_SUPABASE_ANON_KEY,
  SUPABASE_SERVICE_ROLE_KEY:
    statusVars.SERVICE_ROLE_KEY ?? existing.SUPABASE_SERVICE_ROLE_KEY,
  ADMIN_EMAIL: existing.ADMIN_EMAIL ?? example.ADMIN_EMAIL ?? "admin@polla.local",
  ADMIN_PASSWORD:
    existing.ADMIN_PASSWORD ?? example.ADMIN_PASSWORD ?? "localdev123",
  VITE_CURRENCY: existing.VITE_CURRENCY ?? example.VITE_CURRENCY ?? "COP",
  VITE_LOCALE: existing.VITE_LOCALE ?? example.VITE_LOCALE ?? "es-CO",
};

if (!merged.VITE_SUPABASE_ANON_KEY || !merged.SUPABASE_SERVICE_ROLE_KEY) {
  console.error("Missing ANON_KEY or SERVICE_ROLE_KEY from supabase status.");
  process.exit(1);
}

writeFileSync(ENV_FILE, serializeEnv(merged));
console.log(`Wrote ${ENV_FILE}`);
console.log("");
console.log("Next steps:");
console.log("  pnpm seed:admin:local   # create admin user");
console.log("  pnpm dev                # Vite loads .env.local automatically");
